public class Inheritance {
    public static void main(String[] args) {
        Object o1 = new Object();
        Object o2 = new Object();
        System.out.println(o1.hashCode());//1072591677
        System.out.println(o2.hashCode());//1523554304
        System.out.println(o1.toString());//java.lang.Object@3fee733d
    }
}
